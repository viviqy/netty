package com.fairychar.netty.learning.server.hander;

import io.netty.channel.ChannelHandler;
import io.netty.channel.ChannelHandlerContext;
import io.netty.channel.SimpleChannelInboundHandler;

/**
 * Datetime: 2021/6/24 12:50 <br>
 *
 * @author chiyo <br>
 * @since 1.0
 */
@ChannelHandler.Sharable
public class ThrowHandler extends SimpleChannelInboundHandler<String> {
    @Override
    protected void channelRead0(ChannelHandlerContext ctx, String msg) throws Exception {
        if ("1\r\n".equals(msg)) {
            throw new NullPointerException("1");
        } else if ("2\r\n".equals(msg)) {
            throw new ArrayIndexOutOfBoundsException("2");
        }
    }
}
